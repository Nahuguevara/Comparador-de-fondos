def get_gbp_usd_rate():
    """Obtiene el tipo de cambio GBP/USD desde múltiples fuentes"""
    try:
        end_date = pd.Timestamp.now()
        start_date = end_date - pd.DateOffset(days=30)

        # Intentar múltiples fuentes
        sources = [
            ('DEXUSUK', 'fred', 'DEXUSUK'),
            ('GBPUSD=X', 'yahoo', 'Close')
        ]

        for symbol, source, col in sources:
            try:
                data = web.DataReader(symbol, source, start_date, end_date)
                if not data.empty:
                    return data[col].dropna().iloc[-1]
            except:
                continue

        return 1.28  # Backup
    except:
        return 1.28

def parse_fund_size(fund_size_text):
    """Parsea el texto del fund size y convierte a USD"""
    patterns = [
        r'([\d,\.]+)\s*(bn|billion|m|million|k|thousand)\s*(GBP|gbp)',
        r'([\d,\.]+)\s*(GBP|gbp)\s*(bn|billion|m|million|k|thousand)',
        r'([\d,\.]+)(bn|m|k)\s*<[^>]*>GBP',
        r'([\d,\.]+)\s*(bn|m|k)'
    ]

    for pattern in patterns:
        match = re.search(pattern, fund_size_text, re.IGNORECASE)
        if match:
            try:
                value = float(match.group(1).replace(',', ''))
                multiplier = match.group(2).lower() if len(match.groups()) >= 2 else 'm'

                multipliers = {'bn': 1000, 'billion': 1000, 'm': 1, 'million': 1, 'k': 0.001, 'thousand': 0.001}
                value_gbp_millions = value * multipliers.get(multiplier, 1)

                return value_gbp_millions * get_gbp_usd_rate()
            except ValueError:
                continue
    return None

def get_ft_data(isin):
    """Extrae ratings, fund size y launch date de Financial Times"""
    try:
        # Ratings desde página de ratings
        ratings_url = f"https://markets.ft.com/data/funds/tearsheet/ratings?s={isin}:USD"
        resp = requests.get(ratings_url, timeout=10)
        soup = BeautifulSoup(resp.text, "html.parser")

        # Morningstar Stars & Category
        ms_stars = category = None
        ms_app = soup.find("div", {"data-module-name": "MorningstarRatingApp"})
        if ms_app:
            highlighted = ms_app.select_one("span[data-mod-stars-highlighted]")
            ms_stars = len(highlighted.find_all("i", class_="mod-icon--star--filled")) if highlighted else None

            cat_div = ms_app.find("div", class_="mod-morningstar-rating-app__category")
            if cat_div:
                spans = cat_div.find_all("span")
                category = spans[1].get_text(strip=True) if len(spans) > 1 else None

        # Lipper Rating
        lr_rating = None
        lr_app = soup.find("div", {"data-module-name": "LipperRatingApp"})
        if lr_app:
            table = lr_app.find("table", class_="mod-ui-table")
            if table and table.thead and table.tbody:
                headers = [th.get_text(strip=True) for th in table.thead.find_all("th")]
                if "Total return" in headers:
                    col_idx = headers.index("Total return")
                    for row in table.tbody.find_all("tr"):
                        label_cell = row.find("td", class_="mod-ui-table__cell--text")
                        if label_cell and label_cell.get_text(strip=True) == "Overall rating":
                            cells = row.find_all("td")
                            if len(cells) > col_idx:
                                cell = cells[col_idx]
                                icon = cell.find("i")
                                if icon and icon.get("class"):
                                    for c in icon["class"]:
                                        if c.startswith("mod-sprite-lipper-") and c.split("-")[-1].isdigit():
                                            lr_rating = int(c.split("-")[-1])
                                            break
                            break

        # Fund Size y Launch Date desde summary
        summary_url = f"https://markets.ft.com/data/funds/tearsheet/summary?s={isin}:USD"
        resp_summary = requests.get(summary_url, timeout=10)
        soup_summary = BeautifulSoup(resp_summary.text, "html.parser")

        fund_size_usd = launch_date = None
        for table in soup_summary.find_all("table", class_="mod-ui-table--two-column"):
            for row in table.find_all("tr"):
                th = row.find("th")
                if th:
                    header_text = th.get_text(strip=True).lower()
                    td = row.find("td")
                    if td:
                        cell_content = td.get_text(strip=True)

                        if "fund size" in header_text:
                            fund_size_usd = parse_fund_size(cell_content)
                        elif header_text == "launch date":
                            launch_date = cell_content
                            break

        return ms_stars, category, lr_rating, fund_size_usd, launch_date

    except Exception as e:
        print(f"Error procesando ISIN {isin}: {e}")
        return None, None, None, None, None

