def format_table_data(ratings_table):
    """Formatea los datos de la tabla de ratings"""
    ratings_table = ratings_table.fillna('-')

    # Formatear valores
    for col in ['MS Stars', 'Lipper Rating']:
        ratings_table[col] = ratings_table[col].apply(
            lambda x: f"{int(x)}" if pd.notna(x) and x != '-' else '-'
        )

    # Formatear Fund Size en MILLONES
    def format_fund_size(value):
        if pd.isna(value) or value == '-':
            return '-'
        try:
            # Formatear con separador de miles y 1 decimal
            return f"{value:,.1f}"
        except:
            return '-'

    ratings_table['Fund Size'] = ratings_table['Fund Size'].apply(format_fund_size)

    return ratings_table

def calculate_dynamic_widths(data, headers, num_funds):
    """Calcula anchos dinámicos con tabla expandida"""

    # Calcular longitudes máximas por columna
    max_fund_name_length = 0
    max_category_length = 0

    for row in data:
        if row[0] is not None:  # Fondo
            max_fund_name_length = max(max_fund_name_length, len(str(row[0])))
        if len(row) > 3 and row[3] is not None:  # Categoría
            max_category_length = max(max_category_length, len(str(row[3])))

    # Anchos absolutos basados en contenido real
    if max_fund_name_length <= 25:
        fondo_width = 0.25
    elif max_fund_name_length <= 35:
        fondo_width = 0.30
    else:
        fondo_width = 0.35

    if max_category_length <= 20:
        category_width = 0.18
    else:
        category_width = 0.22

    # Anchos fijos optimizados
    col_widths = [
        fondo_width,    # Fondo - dinámico
        0.14,           # ISIN
        0.08,           # Morningstar Stars
        category_width, # Categoría - dinámico
        0.06,           # Lipper Rating
        0.08,           # Fund Size
        0.11            # Share Class Inception
    ]

    return col_widths



