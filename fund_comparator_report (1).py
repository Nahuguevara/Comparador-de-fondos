    years = sorted(df.index.year.unique())

    # =================================================================
    # CALCULAR DATOS DE RECUPERACIÓN
    # =================================================================

    all_results = []
    recovery_matrix = {fund: {} for fund in funds}

    for fund in funds:
        fund_data = df[fund].dropna()
        if len(fund_data) == 0:
            continue

        running_max = fund_data.cummax()
        drawdown_series = (fund_data - running_max) / running_max

        for year in years:
            year_mask = fund_data.index.year == year
            year_data = fund_data[year_mask]

            if len(year_data) == 0:
                continue

            year_dd = drawdown_series[year_mask]
            mdd_value = year_dd.min()

            if pd.isna(mdd_value) or abs(mdd_value) < 0.001:
                # No hubo drawdown significativo en este año → 0 días
                recovery_matrix[fund][year] = 0
                continue

            mdd_date = year_dd.idxmin()
            nav_at_mdd = fund_data.loc[mdd_date]
            peak_value = running_max.loc[mdd_date]

            # Encontrar fecha del peak
            peak_dates = fund_data[fund_data.index <= mdd_date][fund_data >= peak_value * 0.9999]
            if len(peak_dates) > 0:
                peak_date = peak_dates.index[-1]
            else:
                peak_date = fund_data[fund_data.index <= mdd_date].idxmax()
                peak_value = fund_data.loc[peak_date]

            # Buscar recuperación
            post_mdd = fund_data[fund_data.index > mdd_date]
            recovery_mask = post_mdd >= peak_value

            if recovery_mask.any():
                recovery_date = post_mdd[recovery_mask].index[0]
                recovery_days = (recovery_date - mdd_date).days
                nav_at_recovery = fund_data.loc[recovery_date]
            else:
                recovery_date = None
                recovery_days = None
                nav_at_recovery = None

            recovery_matrix[fund][year] = recovery_days

            all_results.append({
                'Fondo_Code': fund,
                'Fondo': name_map.get(fund[:7], fund),
                'Año': year,
                'Peak_Date': peak_date,
                'Peak_NAV': peak_value,
                'MDD_Date': mdd_date,
                'MDD_NAV': nav_at_mdd,
                'DD_Pct': mdd_value * 100,
                'Recovery_Date': recovery_date,
                'Recovery_NAV': nav_at_recovery,
                'Recovery_Days': recovery_days,
            })

    results_df = pd.DataFrame(all_results)

    # Crear matriz de días de recuperación
    recovery_df_temp = pd.DataFrame(recovery_matrix).T
    recovery_df_temp.index = [name_map.get(f[:7], f) for f in recovery_df_temp.index]
